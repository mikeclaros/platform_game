#pragma once
#include "stdafx.h"
#include "VisibleObject.h"


////////////////////////////////
/// @file	Camera.h
////////////////////////////////



//////////////////////////////////////////////////////////////////////
/// @class	Camera
/// @brief	A container for sf::view 
///
/// @author	Mike Claros
/// @date	4/22/2016
///
/// Container for sf::view to be placed into ObjectManager objects
/////////////////////////////////////////////////////////////////////
class Camera : public VisibleObject{
public:
	
	

	/// Constructor
	Camera();
	/// Destructor
	~Camera();

	////////////////////////////////////////////
	//SETTERS

	////////////////////////////////////////////
	/// @brief		sets View for Camera
	///	@param[in]	camera an sf::View object	
	////////////////////////////////////////////
	void setCamera(sf::View camera);

	////////////////////////////////////////////
	/// @brief		sets Center for Camera
	///	@param[in]	cx float, x-value of center
	/// @param[in]	cy float, y-value of center
	////////////////////////////////////////////
	void setCenter(float cx, float cy);

	////////////////////////////////////////////
	/// @brief		sets Size for Camera
	///	@param[in]	width	float, set width
	/// @param[in]	height	float, set height
	////////////////////////////////////////////
	void setSize(float width, float height);



	////////////////////////////////////
	//GETTERS


	////////////////////////////////////////////
	/// @brief		get view object
	///	@return		sf::View object
	////////////////////////////////////////////
	sf::View getCamera();

	


	///////////////////////////////////
	//UPDATES 

	/////////////////////////////////////////////////
	/// @brief		update function called per frame
	///	@param[in]	elapsedTime Time in seconds
	/////////////////////////////////////////////////
	
	void update(float elapsedTime);


private:


	////////////////////////////////
	//ATTRIBUTES

	float _position;
	float _target;
	float _size;
	float _speed;
	sf::View _camera;
};
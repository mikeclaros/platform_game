///////////////////////////////////////////////////
/// @class mainMenu
///	@brief menu interface
///
///
//////////////////////////////////////////////////
class mainMenu{
public:


	

	////////////////////////////////////////////
	/// @brief		Enum, Menuresults
	////////////////////////////////////////////
	enum MenuResult{
		Nothing,	///< Nothing: no result
		Start,		///< Start	: start game
		Exit		///< Exit	: exit game
	};


	
	
	////////////////////////////////////////////
	/// @class	MenuItem
	///	@brief	MenuItem struct for menu buttons
	////////////////////////////////////////////
	struct MenuItem{
	public:
		sf::Rect<int> rect;			///< rect for size of button
		MenuResult action;			///< actions
	};


	////////////////////////////////////////////
	/// @brief shows window
	/// @param[in]	window takes in main window
	////////////////////////////////////////////
	MenuResult show(sf::RenderWindow& window);

private:

	////////////////////////////////
	//ATTRIBUTES

	MenuResult getMenuResponse(sf::RenderWindow& window);
	MenuResult handleClick(int x, int y);
	std::list<MenuItem> _menuItems;
};
